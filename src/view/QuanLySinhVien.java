    public void setModel(SinhVien nv){
        txtmanv.setText(nv.getMaSV());
        txttennv.setText(nv.getTenSV());
        txtngaysinh.setText(date_format.format(nv.getNgaySinh()));
        txtDiachi.setText(nv.getDiachi());
        if(nv.isGioitinh()){
            rdnam.isSelected();
        }else{
            rdnu.isSelected();
        }
        //load hinh anh
        if(nv.getHinhanh().equalsIgnoreCase("no avata")){
            blhinhanh.setText("NO AVATA");
            blhinhanh.setIcon(null);
            
        }else{
            blhinhanh.setText("");
        ImageIcon imgIcon = new ImageIcon(getClass().getResource("/images/"+nv.getHinhanh()));
        Image img=imgIcon.getImage();
        img.getScaledInstance(blhinhanh.getWidth(), blhinhanh.getY(), 0);
        blhinhanh.setIcon(imgIcon);
            
        }
        
    }
    private void tblnhanvienMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblnhanvienMouseClicked
        // TODO add your handling code here:
        int id = tblnhanvien.rowAtPoint(evt.getPoint());
        String masv;
        masv = tblnhanvien.getValueAt(id, 0).toString();
        SinhVien nv =dao.getSinhVienByID(masv);
        setModel(nv);
    }//GEN-LAST:event_tblnhanvienMouseClicked

    private void btnsuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsuaActionPerformed
        // TODO add your handling code here:
        if(validateForm()){
            try {
                SinhVien nv = getModel();
                if(dao.updateSinhVienByID(nv)>0){
                    JOptionPane.showMessageDialog(this, "Cap nhat thanh cong");
                    filldataTable();
                }
            } catch (ParseException ex) {
                Logger.getLogger(QuanLySinhVien.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }else{
            JOptionPane.showMessageDialog(this, "Ban chua nhap du thong tin");
        }
    }//GEN-LAST:event_btnsuaActionPerformed

