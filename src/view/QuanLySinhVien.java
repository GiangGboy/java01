    public void filldataTable(){
        DefaultTableModel model = (DefaultTableModel)tblnhanvien.getModel();
        model.setRowCount(0);
        for(SinhVien nv: dao.getALLSinhVien()){
            Object rowData[] = new Object[6];
            rowData[0]=nv.getMaSV();
            rowData[1]=nv.getTenSV();
            rowData[2]=date_format.format(nv.getNgaySinh());
            rowData[3]=nv.isGioitinh()?"NAM":"NU";
            rowData[4]=nv.getDiachi();
            rowData[5]=nv.getHinhanh();
            model.addRow(rowData);
        }
    }

