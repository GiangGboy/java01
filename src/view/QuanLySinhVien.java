    public boolean validateForm(){
        if(txtmanv.getText().isEmpty()
                || txttennv.getText().isEmpty()){
            return false;
            
        }
        return true;
    }
    public SinhVien getModel() throws ParseException{
        SinhVien nv = new SinhVien();
        nv.setMaSV(txtmanv.getText());
        nv.setTenSV(txttennv.getText());
        boolean gt = false;
        if(rdnam.isSelected()){
            gt = true;//Nam
        }
        nv.setGioitinh(gt);
        nv.setDiachi(txtDiachi.getText());
        nv.setNgaySinh(date_format.parse(txtngaysinh.getText()));
        if(strhinhanh == null){
            nv.setHinhanh("No avata");
            
        }else{
            nv.setHinhanh(strhinhanh);
        }
        
        return nv;
        
        
        
        
        
        
        
        
        
        
        
    }
